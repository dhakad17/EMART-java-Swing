    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        String userid=jcReceptionist.getSelectedItem().toString();
        try {
            boolean result=ReceptionistDAO.deleteReceptionist(userid);
            
            if (result) {
                  JOptionPane.showMessageDialog(null, "Receptionist Removed Successfully !!");
                  jcReceptionist.removeItemAt(jcReceptionist.getSelectedIndex());
                  if (jcReceptionist.getItemCount()==0) {
                    btnRemove.setEnabled(false);
                    return;
                }
                    JOptionPane.showMessageDialog(null, "Receptionist not deleted :( !!");
            }
        } catch (SQLException e) {
              JOptionPane.showMessageDialog(null, "DB Error in btn Remove method  !!");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

