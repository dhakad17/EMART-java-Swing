    private void loadReceptionistId()
    {
        try {
            List<String > receptionistid=ReceptionistDAO.getAllReceptionistUserId();
            if (receptionistid.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No receptionist is present !!");
                return;
            }
            btnRemove.setEnabled(true);
            
            for(String userid:receptionistid)
            {
                jcReceptionist.addItem(userid);
            }
           
        } catch (SQLException e) {
              JOptionPane.showMessageDialog(null, "DB error in load method!!");
        }
    }

