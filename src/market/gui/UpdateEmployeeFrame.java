    private void getEmployees()
    {
        try {
            jcEmplid.removeAllItems();
            List<String> allEmpid=employeeDAO.getAllEmpID();
            
            for(String id:allEmpid)
            {
                jcEmplid.addItem(id);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error in getting All Employee ID:","error",JOptionPane.ERROR_MESSAGE);
        }
    }
    private boolean validateInput()
    {
        return !(txtEmpName.getText().isEmpty()||txtEmpSal.getText().isEmpty());
    }
