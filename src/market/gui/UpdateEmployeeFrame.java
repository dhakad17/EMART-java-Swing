    private void jcEmplidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmplidActionPerformed
        // TODO add your handling code here:
        try {
            String empid=jcEmplid.getSelectedItem().toString();
            EmployeesPojo e=employeeDAO.findEmpById(empid);
            
            txtEmpName.setText(e.getEmpname());
            txtEmpSal.setText(String.valueOf(e.getSalary()));
            
            jcbJob.setSelectedItem(e.getJob());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error in action permoes of empid ","error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jcEmplidActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (!validateInput()) {
            JOptionPane.showMessageDialog(null,"Please input all the values","error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            EmployeesPojo e=new EmployeesPojo();
            e.setEmpid(jcEmplid.getSelectedItem().toString());
            e.setEmpname(txtEmpName.getText());
            System.out.println("complete1");
            e.setJob(jcbJob.getSelectedItem().toString());
            e.setSalary(Double.parseDouble(txtEmpSal.getText()));
             System.out.println("complete2");
            boolean result=employeeDAO.updateEmployee(e);
             System.out.println("complete3");
            if (result) {
                JOptionPane.showMessageDialog(null, "Update successfully :)","Success",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "updation failed :(","error",JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error while Updating data :(","error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

